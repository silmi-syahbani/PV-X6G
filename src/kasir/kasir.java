        private void koneksi() {
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/project1", "root", "");
            stat = con.createStatement();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Koneksi Gagal: " + e.getMessage());
        }
    }
        
        private void tampilData() {
        Object[] baris = {"ID", "Nama", "Gender", "Telepon", "Agama", "Alamat", "Password"};
        model = new DefaultTableModel(null, baris);
        jTable.setModel(model); 
        
        try {
            res = stat.executeQuery("SELECT * FROM kasir");
            while (res.next()) {
                model.addRow(new Object[]{
                    res.getString("id_kasir"),
                    res.getString("nm_kasir"),
                    res.getString("jenis_kelamin"),
                    res.getString("no_telepon"),
                    res.getString("agama"),
                    res.getString("alamat"),
                    res.getString("password")
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

