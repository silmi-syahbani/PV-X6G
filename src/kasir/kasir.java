    private void jTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableMouseClicked
   int baris = jTable.getSelectedRow();
    

    String id        = String.valueOf(jTable.getValueAt(baris, 0));
    String nama      = String.valueOf(jTable.getValueAt(baris, 1));
    String gender    = String.valueOf(jTable.getValueAt(baris, 2));
    String telepon   = String.valueOf(jTable.getValueAt(baris, 3));
    String agama     = String.valueOf(jTable.getValueAt(baris, 4));
    String alamat    = String.valueOf(jTable.getValueAt(baris, 5));
    String password  = String.valueOf(jTable.getValueAt(baris, 6));

    txtid.setText(id);
    txtnm.setText(nama);
    txttlp.setText(telepon);
    txtalamat.setText(alamat);
    txtpass.setText(password);

    if (gender.equals("Laki-Laki")) {
        rblaki.setSelected(true);
    } else if (gender.equals("Perempuan")) {
        rbperempuan.setSelected(true);
    }

  
   cboxagama.setSelectedItem(agama);       // TODO add your handling code here:
    }//GEN-LAST:event_jTableMouseClicked

